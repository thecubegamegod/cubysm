function newMap() {
  if (currentMap != 2) {
    mode = 4;
    let leaderboard = [];
    max = 99;
    tempmax = 0;
    for (m = 0; m <= max; m++) {
      for (j = 0; j <= pos.length - 1; j++) {
        if (pos[j].points == m) {
          leaderboard.unshift(pos[j].name + " - " + pos[j].points);
        }
        if (max != tempmax) {
          if (pos[j].points > tempmax) {
            tempmax = pos[j].points;
          }
        }
      }
      max = tempmax;
    }
    first = "ðŸ¥‡ " + leaderboard[0];
    second = "ðŸ¥ˆ " + leaderboard[1];
    third = "ðŸ¥‰ " + leaderboard[2];
    io.sockets.emit("results", [first, second, third]);
    currentMap = 2;
    map = maps[currentMap];
  } else {
    if (currentMap == 0) {
      currentMap = 1;
    } else if (currentMap == 1) {
      currentMap = 0;
    } else {
      currentMap = 0;
    }
    map = maps[currentMap];
    aliveplayers = [];
    for (i = 0; i < pos.length; i++) {
      if (pos[i].dead == 0) {
        aliveplayers.push(pos[i]);
      }
      pos[i].points = 0;
      pos[i].vip = false;
    }
    if (aliveplayers.length > 1) {
      let tempmode = mode;
      while (tempmode == mode) {
        tempmode = Math.floor(Math.random() * (3 - 1)) + 2;
        if (tempmode == 4) {
          tempmode = 1;
        }
      }
      mode = tempmode;
    } else {
      mode = 0;
    }
    if (mode == 2) {
      vip = aliveplayers[Math.floor(Math.random() * (aliveplayers.length - 1)) + 0].id;
      pos[vip].vip = true;
    } else if (mode == 3) {
      checkPointStuck();
    }
  }

  mapCountdown = modes[mode].time * 60;
  io.sockets.emit("changemap", [currentMap, mode]);
}

function checkPointStuck() {
  let stuck = true;
  let stuckloop = false;
  while (stuck == true) {
    stuckloop = false;
    pointX = Math.floor(Math.random() * (2000 + 2000)) + -2000;
    pointY = Math.floor(Math.random() * (2000 + 2000)) + -2000;
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 40; j++) {
        if (map[j][i] == 1) {
          if (pointX >= i * 100 - 2000 && pointX <= i * 100 - 2000 + 100 && j * 100 - 2000 <= pointY && j * 100 - 2000 + 100 >= pointY) {
            stuckloop = true;
          }

          if (pointY >= j * 100 - 2000 && pointY <= j * 100 - 2000 + 100 && i * 100 - 2000 <= pointX && i * 100 - 2000 + 100 >= pointX) {
            stuckloop = true;
          }
        }
        if (stuckloop == true) {
          break;
        }
      }
    }
    if (stuckloop == false) {
      stuck = false;
    }
  }
  io.sockets.emit("pointLocationSet", [pointX, pointY]);
}

